package by.academy.classwork.oop.task7.details;

public class Engine {
    private int power;
    private int manufacturer;

    public Engine(int power, int manufacturer) {
        super();
        this.power = power;
        this.manufacturer = manufacturer;
    }

    public int getPower() {
        return power;
    }

    public int getManufacturer() {
        return manufacturer;
    }
}
